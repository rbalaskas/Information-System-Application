    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        
        model = (DefaultTableModel)jTable1.getModel();
        
        int rowindex = jTable1.getSelectedRow();
        
        female_radio.setSelected(false);
        male_radio.setSelected(false);
        
        
        id_textfield.setText(model.getValueAt(rowindex, 0).toString());
        firstname_textfield.setText(model.getValueAt(rowindex, 1).toString());
        lastname_textfield.setText(model.getValueAt(rowindex, 2).toString());
        if(model.getValueAt(rowindex, 3).toString().equals("Male")){
            male_radio.setSelected(true);
        }
        else{
            female_radio.setSelected(true);
        }
        
        Date bdate;
        try {
            bdate = new SimpleDateFormat("MM/dd/yy").parse(model.getValueAt(rowindex, 4).toString());
            date_chooser.setDate(bdate);
        } catch (ParseException ex) {
            Logger.getLogger(manageStudentsForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        phone_textfield.setText(model.getValueAt(rowindex, 5).toString());
        emergencyphone_textfield.setText(model.getValueAt(rowindex, 8).toString());
        email_textfield.setText(model.getValueAt(rowindex, 6).toString());
        address_textarea.setText(model.getValueAt(rowindex, 7).toString());
        
        
    }//GEN-LAST:event_jTable1MouseClicked

