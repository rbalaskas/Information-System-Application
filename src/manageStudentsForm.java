    public boolean verifytext() {
	if (firstname_textfield.getText().equals("") || lastname_textfield.getText().equals("") 
                    || phone_textfield.getText().equals("") || emergencyphone_textfield.getText().equals("") || address_textarea.getText().equals("")
                    || id_textfield.getText().equals("") ||date_chooser.getDate() == null) {
			
                JOptionPane.showMessageDialog(null, "One Or More Empty Field!");
                return false;
	}
        else if(date_chooser.getDate().compareTo(new Date()) > 0){
                JOptionPane.showMessageDialog(null, "we are not allow student from the future!");
                return false;
        }
	else {
               return true;
	}
    }
    
    
