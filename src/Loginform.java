    private void login_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_login_buttonActionPerformed
        if(username_text.getText().equals("")) {
           //username_star.setVisible(true);
           JOptionPane.showMessageDialog(null,"Username Field is Empty", null,2);
        }
        else if(String.valueOf(password_text.getPassword()).equals("")) {
            //password_star.setVisible(true);
            JOptionPane.showMessageDialog(null,"Password Field is Empty", null, 2);
        }
        else {

            Connection con = MyConnection.getConnection();
            PreparedStatement ps;
            try {
                ps = con.prepareStatement("SELECT * FROM user WHERE username = ? AND password = ?");

                ps.setString(1, username_text.getText());
                ps.setString(2, String.valueOf(password_text.getPassword()));

                ResultSet rs = ps.executeQuery();

                if(rs.next()) {
                    setVisible(false);
                    Mainform mf = new Mainform();
                    mf.setVisible(true);
                    mf.pack();
                    mf.setTitle("Main Page");
                    mf.setLocationRelativeTo(null);
                    Mainform.welcome_label.setText("Welcome " + username_text.getText());
                    Mainform.studentscount_label.setText(" Student count = "+Integer.toString(MyFunction.countData("student")));
                    Mainform.coursesscount_label.setText(" Course count = "+Integer.toString(MyFunction.countData("course")));
                    this.dispose();
                }
                else {
                    JOptionPane.showMessageDialog(null,"Incorrect Username Or Password", "Login Failed", 2);
                }

            } catch (SQLException ea) {
                System.out.println(ea.getMessage());
            }
        }

    }//GEN-LAST:event_login_buttonActionPerformed

