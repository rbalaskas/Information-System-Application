    private void add_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_buttonActionPerformed

       int stdid = Integer.valueOf(studentid_textfield.getText());
       int crsid = Integer.valueOf(c.getcourseid(courseid_combobox.getSelectedItem().toString()));
       String used_item = String.valueOf(useditem_jComboBox.getSelectedItem().toString());
       SimpleDateFormat dateFormat = new SimpleDateFormat();
       String date = dateFormat.format(date_chooser.getDate());
       
       es.insertUpdateDeleteStudent('i', stdid, crsid, used_item, date);
       
       studentid_textfield.setText("");
       date_chooser.setDate(null);

    }//GEN-LAST:event_add_buttonActionPerformed

