    public boolean verifytext() {
	if (firstname_textfield.getText().equals("") || lastname_textfield.getText().equals("") 
                    || phone_textfield.getText().equals("")  || address_textarea.getText().equals("")
                    || date_chooser.getDate() == null || email_textfield.getText().equals("") || emergencyphone_textfield.getText().equals("")) {
			
                JOptionPane.showMessageDialog(null, "One Or More Empty Field!");
                return false;
	}
        else if(date_chooser.getDate().compareTo(new Date()) > 0){
                JOptionPane.showMessageDialog(null, "we are not allow student from the future!");
                return false;
        }
	else {
               return true;
	}
    }
    
    
    private void add_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_buttonActionPerformed

        String fname = firstname_textfield.getText();
        String lname = lastname_textfield.getText();
        
        String gender = "Male";
        if(female_radio.isSelected()) {
            gender = "Female";
        }
        String phone = phone_textfield.getText();
	String emergencyphone = emergencyphone_textfield.getText();
        String address = address_textarea.getText();
        String email = email_textfield.getText();
       
        
        if(verifytext()){
            SimpleDateFormat dateFormat = new SimpleDateFormat();
            String bdate = dateFormat.format(date_chooser.getDate());
            student std = new student();
            std.insertUpdateDeleteStudent('i', null, fname, lname, gender, bdate, phone, email, address, emergencyphone);
            Mainform.studentscount_label.setText(" Students count = "+Integer.toString(MyFunction.countData("student")));
           
        }
        try{
            manageStudentsForm.jTable1.setModel(new DefaultTableModel(null,new Object[]{"id","first_name", "last_name", "gender", "birthdate", "phone", "email", "address", "emergencyphone"}));
            std.fillStudentJtable(manageStudentsForm.jTable1, "");
            
        }
        catch(Exception ex){
            System.out.println(ex.getMessage());
        }
                
        
	
    }//GEN-LAST:event_add_buttonActionPerformed

