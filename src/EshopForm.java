    private void buy_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buy_buttonActionPerformed

        int stdid = Integer.valueOf(studentid_textfield.getText());
        String itm = item_textfield.getText();
        int price = Integer.valueOf(price_textfield.getText());
        String size = size_textfield.getText();
        String Promitheutis = Promitheutis_textfield.getText();
        String Code = Code_textfield.getText();
    
        SimpleDateFormat dateFormat = new SimpleDateFormat();
        String date = dateFormat.format(date_chooser.getDate());
        

        es.insertUpdateDeleteStudent('i', stdid, itm, price, size, date, Promitheutis, Code);

        studentid_textfield.setText("");
        item_textfield.setText("");
        price_textfield.setText("");
        size_textfield.setText("");
        date_chooser.setDate(null);
        Promitheutis_textfield.setText("");
        Code_textfield.setText("");
    }//GEN-LAST:event_buy_buttonActionPerformed

