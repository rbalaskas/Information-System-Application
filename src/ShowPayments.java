    void getSum(){
        int sum = 0;
        for (int i = 0; i<jTable1.getRowCount(); i++){
            sum = sum+ Integer.parseInt(jTable1.getValueAt(i, 4).toString());
        }
        JLabel label = new JLabel("Yearly income is : â¬"+ sum);
        label.setFont(new Font("Arial", Font.BOLD, 18));
        JOptionPane.showMessageDialog(null,label );
    }
    
    void getMonthSum(){
        int sum = 0;
        for (int i = 0; i<jTable1.getRowCount(); i++){
            if(jTable1.getValueAt(i, 3).toString().equals(month_jComboBox.getSelectedItem().toString())){
                sum = sum+ Integer.parseInt(jTable1.getValueAt(i, 4).toString());
            }
        }
        JLabel label = new JLabel("Income for month " + month_jComboBox.getSelectedItem().toString() + " is : â¬"+ sum);
        label.setFont(new Font("Arial", Font.BOLD, 18));
        JOptionPane.showMessageDialog(null,label );
    }
    
    
