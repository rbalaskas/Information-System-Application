    private void edit_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_edit_buttonActionPerformed
        
       int sid = Integer.valueOf(studentid_textfield.getText());
       int cid = Integer.valueOf(courseid_textfield.getText());
       double scr = Double.valueOf(score_textfield.getText());
       String ds = description_textfield.getText();
       
       sc.insertUpdateDeleteScore('u', sid, cid, scr, ds);
       jTable1.setModel(new DefaultTableModel(null, new Object[]{"student_id","course_id","student_score","description"}));
       sc.fillScoreJtable(jTable1);
       
    }//GEN-LAST:event_edit_buttonActionPerformed

