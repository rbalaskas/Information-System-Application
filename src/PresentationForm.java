    private void sendemail(){
        
       
        String host="smtp.gmail.com";  
        final String user = "k1fightclub2020@gmail.com";
        final String password="VaggelisK1!@";

        String to = mail;

    //imported code
        Properties props = new Properties(); 
        props.put("mail.smtp.host", "smtp.gmail.com"); 
        props.put("mail.smtp.auth", "true"); 
        props.put("mail.smtp.port", "587"); 
        props.put("mail.smtp.starttls.enable", "true"); 
        props.put("mail.smtp.ssl.trust", "*");

        Session session = Session.getDefaultInstance(props,  
        new javax.mail.Authenticator() {  
            protected PasswordAuthentication getPasswordAuthentication() {  
                return new PasswordAuthentication(user,password);  
            }  
        });  

        
        try {  
         MimeMessage message = new MimeMessage(session);  
         message.setFrom(new InternetAddress(user));  
         message.addRecipient(Message.RecipientType.TO, new InternetAddress(to));  
         message.setSubject("Presentations for Vaggelis Moustakas K1 Fight Club");  
         message.setText("Hello,\n" +"The student with id " + studentid_textfield.getText().toString() + " is not presented at lesson " + courseid_combobox.getSelectedItem().toString()+ " for the date "+date_chooser.getDate().toString()+"!"+"\n"+" Thank you, Vaggelis Moustakas!");  


         Transport.send(message);  

         System.out.println("message sent!");  

        } 
        catch (MessagingException mex) 
        {
            System.out.println("Error: unable to send message....");
            mex.printStackTrace();
        }
    }
    
    
